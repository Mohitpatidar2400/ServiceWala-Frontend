// TODO remove, this demo shouldn't need to reset the theme.

const defaultTheme = createTheme();

export default function Service_Login () {
  const [email,setEmail] = useState("");
  const [password,setPassword] = useState("");
  const {isSAuthenticated,setIsSAuthenticated,service,setService} = useContext(Context);
  const navigate = useNavigate();
  const handleSubmit = async (event) => {
    event.preventDefault();
    const data = new FormData(event.currentTarget);
    setEmail(data.get('email'));
    setPassword(data.get('password'));

    try {
      const { data } = await axios.post(`${SERVER}/service_provider/service_provider_login`, {
          email,password,
      }, {
          headers: {
              "Content-Type": "application/json",
          },
          withCredentials: true,
      });
      setIsSAuthenticated(true);
      setService(data.user);
      localStorage.setItem("authTokenService","yourAuthTokenValue");
      localStorage.setItem("Service_Provider", data.user.email);
      navigate("/service_homepage")
  } catch (error) {
      console.log("Something Happened!", error.response.data);
  }

  };

  return (
    <ThemeProvider theme={defaultTheme}>
      <Container component="main" maxWidth="xs">
        <CssBaseline />
        <Box
          sx={{
            marginTop: 8,
            display: 'flex',
            flexDirection: 'column',
            alignItems: 'center',
          }}
        >
          <Avatar sx={{ m: 1, bgcolor: 'secondary.main' }}>
            <LockOutlinedIcon />
          </Avatar>
          <Typography component="h1" variant="h5">
            Service Provider Sign in
          </Typography>
          <Box component="form" onSubmit={handleSubmit} noValidate sx={{ mt: 1 }}>
            <TextField
              margin="normal"
              required
              fullWidth
              id="email"
              label="Email Address"
              name="email"
              autoComplete="email"
              autoFocus
            />
            <TextField
              margin="normal"
              required
              fullWidth
              name="password"
              label="Password"
              type="password"
              id="password"
              autoComplete="current-password"
            />
            <Button
              type="submit"
              fullWidth
              variant="contained"
              sx={{ mt: 3, mb: 2 }}
            >
              Sign In
            </Button>
            <Grid container>
              <Grid item>
                <Link href="/service_register" variant="body2">
                  {"Don't have an account? Sign Up"}
                </Link>
              </Grid>
            </Grid>
          </Box>
        </Box>
        <Copyright sx={{ mt: 8, mb: 4 }} />
      </Container>
    </ThemeProvider>
  );
}
